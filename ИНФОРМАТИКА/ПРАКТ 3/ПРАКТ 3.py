# 1. Создание списка
# Списки — упорядоченные изменяемые коллекции элементов, которые могут содержать разные типы данных.
my_list = [1, 2, 3, 4, 5]
print("Созданный список:", my_list)

# 2. Метод split
# Разделяет строку на список по заданному разделителю (по умолчанию — пробел).
string = "Влад Кузнецов Красавчик!"
words = string.split()  # по умолчанию разделяет по пробелам
print("Результат split:", words)

# 3. Индексация элементов списка. Обращение к элементу списка
# Индексы начинаются с 0
first_element = my_list[0]
last_element = my_list[-1]
print("Первый элемент:", first_element)
print("Последний элемент:", last_element)

# 4. Работа со срезами. Границы срезов
# Синтаксис: list[start:stop] — элементы с start до stop-1
slice1 = my_list[1:4]  # элементы с индексами 1,2,3
print("Срез my_list[1:4]:", slice1)

# Границы срезов по умолчанию:
slice2 = my_list[:3]  # с начала до 2-го индекса
slice3 = my_list[2:]  # с 2-го индекса до конца
print("Срез my_list[:3]:", slice2)
print("Срез my_list[2:]:", slice3)

# 5. Срез с шагом
# Синтаксис: list[start:stop:step]
step_slice = my_list[::2]  # каждый второй элемент
print("Срез с шагом 2:", step_slice)

# ===========================
# Методы для работы со списками
# ===========================

# 1. Метод append
# Добавляет элемент в конец списка
my_list.append(6)
print("После append(6):", my_list)

# 2. Сортировка элементов списка. Метод sort
unsorted_list = [3, 1, 4, 2]
unsorted_list.sort()  # по возрастанию
print("Отсортированный список:", unsorted_list)

# 3. Статистические показатели списка: функции min, max, метод count
numbers = [5, 2, 9, 2, 7]
print("Минимум:", min(numbers))
print("Максимум:", max(numbers))
print("Количество 2 в списке:", numbers.count(2))

# 4. Генераторы списков
# Создадим список квадратов чисел от 0 до 9
squares = [x**2 for x in range(10)]
print("Генератор списков (квадраты 0-9):", squares)

# Еще пример: список четных чисел от 0 до 20
evens = [x for x in range(21) if x % 2 == 0]
print("Четные числа от 0 до 20:", evens)

